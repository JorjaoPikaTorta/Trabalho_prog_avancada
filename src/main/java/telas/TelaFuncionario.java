 private void montaTabela() {
        ArrayList<Funcionario> f = ff.recuperarTodos();
        if (f == null) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao consultar funcionarios, verifique os logs.");
        } else {
            tblFuncionarios.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Nome do funcionário";
                        case 1:
                            return "Cpf";
                        case 2:
                            return "Data de nascimento";
                        case 3:
                            return "Cargo";
                            case 4:
                            return "Salário";
                            case 5:
                            return "Status";
                             case 6:
                            return "Idfunc";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 7;
                }

                @Override
                public int getRowCount() {
                    return f.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Funcionario t = f.get(rowIndex);

                    if (t != null) {
                        switch (columnIndex) {
                            case 0:
                                return t.getNomefunc();
                            case 1:
                                return t.getCpf();
                            case 2:
                                return t.getDatanasc();
                            case 3:
                                return t.getCargo();
                                case 4:
                                return t.getSalario();
                                case 5:
                                return t.getStatus();
                                 case 6:
                                return t.getIdfunc();
                        }

                    }

                    return "n/d";
                }
            });
        }
    }
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String nome = txtnmfunc.getText();
        String cpf = txtFormatacaoCpf.getText();
        String cargo = txtcargo.getText();
        String salario = txtsalario.getText();
        String datanasc = txtdtnascfunc.getText();
        String status = txtstatusfunc.getText();

        Funcionario f = new Funcionario();
        f.setNomefunc(nome);
        f.setCpf(cpf);
        f.setDatanasc(datanasc);
        f.setCargo(cargo);
        f.setSalario(salario);
        f.setStatus(status);

      String Cpf = txtFormatacaoCpf.getText();
        String cpfLimpo = Formatacao.removerFormatacao(cpf);

        boolean retorno = false;

        if (codigo == 0) {
            retorno = ff.salvar(f);
        } else {
            f.setIdfunc(codigo);
            retorno = ff.editar(f);
        }

        if (retorno) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            lbl.setText("");
            lbl1.setText("");
            lbl2.setText("");
            lbl3.setText("");
            lbl4.setText("");
            lbl5.setText("");

            lbl.requestFocus();
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

