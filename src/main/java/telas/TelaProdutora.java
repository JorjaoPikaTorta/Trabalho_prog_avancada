    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String razaosocial = txtraso.getText();
        String nomefantasia = txtnmfan.getText();
        String cnpj = txtcnpj.getText();
        String telefone = txttele.getText();
        String email = txtemail.getText();
       

        Produtora p = new Produtora();
        p.setRazaosocial(razaosocial);
        p.setNomefantasia(nomefantasia);
        p.setCnpj(cnpj);
        p.setTelefone(telefone);
        p.setEmail(email);
    

        boolean retorno = pp.salvar(p);

        if (retorno) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            txtraso.setText("");
            txtnmfan.setText("");
            txtcnpj.setText("");
            txttele.setText("");
            txtemail.setText("");
            

            txtraso.requestFocus();
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

