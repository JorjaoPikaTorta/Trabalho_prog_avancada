private void montaTabela() {
        ArrayList<Produtora> p = pp.recuperarTodos();
        if (p == null) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao consultar produtoras, verifique os logs.");
        } else {
            tblprod.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Razão Social";
                        case 1:
                            return "Nome Fantasia";
                        case 2:
                            return "CNPJ";
                        case 3:
                            return "Telefone";
                            case 4:
                            return "Email";
                            case 5:
                            return "Idprod";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 6;
                }

                @Override
                public int getRowCount() {
                    return p.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Produtora t = p.get(rowIndex);

                    if (t != null) {
                        switch (columnIndex) {
                            case 0:
                                return t.getRazaosocial();
                            case 1:
                                return t.getNomefantasia();
                            case 2:
                                return t.getCnpj();
                            case 3:
                                return t.getTelefone();
                                case 4:
                                return t.getEmail();
                                case 5:
                                return t.getIdprod();
                        }

                    }

                    return "n/d";
                }
            });
        }
    }
