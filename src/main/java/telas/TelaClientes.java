    private void montaTabela() {
        ArrayList<Cliente> c = cc.recuperarTodos();
        if (c == null) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao consultar clientes, verifique os logs.");
        } else {
            tblClientes.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Nome";
                        case 1:
                            return "Cpf";
                        case 2:
                            return "Telefone";
                        case 3:
                            return "Endereço";
                            case 4:
                            return "Data de nascimento";
                            case 5:
                            return "Status";
                             case 6:
                            return "Idcliente";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 7;
                }

                @Override
                public int getRowCount() {
                    return c.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Cliente t = c.get(rowIndex);

                    if (t != null) {
                        switch (columnIndex) {
                            case 0:
                                return t.getNome();
                            case 1:
                                return t.getCpf();
                            case 2:
                                return t.getTelefone();
                            case 3:
                                return t.getEndereco();
                                case 4:
                                return t.getDatanasc();
                                case 5:
                                return t.getStatus();
                                 case 6:
                                return t.getIdcliente();
                        }

                    }

                    return "n/d";
                }
            });
        }
    }


